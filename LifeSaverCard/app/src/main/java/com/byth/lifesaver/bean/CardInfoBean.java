public class CardInfoBean {
    private String goodsName;//卡类别
    private String activeTime;//激活时间
    private String expiryDate;//失效时间
    private String distribName;//渠道商
    private String nickName;//持卡人
    private String phone;//绑定手机
    private String idCode;//身份证
    private int gender;//性别：1男，2女
    private String address;//联系地址
    private String linkman;//紧急联系人
    private String tel;//紧急联系人电话
    private String relationship;//与持卡人关系
    private String illItem;//既往病史
    private String drugAllergy;//药物过敏情况
    private String bloodType;//血型
    private String lifeCardStatus;//失效状态   默认 default ，已失效 expired，即将失效toExpiry

    public String getGoodsName() {
        return goodsName;
    }

    public String getActiveTime() {
        return activeTime;
    }

    public String getExpiryDate() {
        return expiryDate;
    }

    public String getDistribName() {
        return distribName;
    }

    public String getNickName() {
        return nickName;
    }

    public String getPhone() {
        return phone;
    }

    public String getIdCode() {
        return idCode;
    }

    public int getGender() {
        return gender;
    }

    public String getAddress() {
        return address;
    }

    public String getLinkman() {
        return linkman;
    }

    public String getTel() {
        return tel;
    }

    public String getRelationship() {
        return relationship;
    }

    public String getIllItem() {
        return illItem;
    }

    public String getDrugAllergy() {
        return drugAllergy;
    }

    public String getBloodType() {
        return bloodType;
    }

    public String getLifeCardStatus() {
        return lifeCardStatus;
    }
}
